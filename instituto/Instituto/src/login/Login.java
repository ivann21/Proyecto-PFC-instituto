     private static void connectToDatabase() {
        try {
            connection = DriverManager.getConnection("jdbc:hsqldb:hsql://localhost/", "SA", "");
            statement = connection.createStatement();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private boolean verificarCredenciales(String correo, String contraseña) {
        try {
            String query = "SELECT * FROM instituto.profesores WHERE CORREOELECTRONICO = '" + correo + "' AND CONTRASENIA = '" + contraseña + "'";
            resultSet = statement.executeQuery(query);
            return resultSet.next();
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
    }

    private int obtenerIdProfesor(String correo) {
        try {
            String query = "SELECT \"ID profesor\" FROM instituto.profesores WHERE CORREOELECTRONICO = '" + correo + "'";
            resultSet = statement.executeQuery(query);
            if (resultSet.next()) {
                return resultSet.getInt("ID profesor");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return -1; // Retorna -1 si no se encuentra la ID
    }

    private String hashPassword(String password) {
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] hashedBytes = md.digest(password.getBytes());
            return Base64.getEncoder().encodeToString(hashedBytes);
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
            return null;
        }
    }
        
     
 
