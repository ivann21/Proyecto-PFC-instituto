         private static void connectToDatabase() {
        try {
            // Establecer la conexión con la base de datos
            connection = DriverManager.getConnection("jdbc:hsqldb:hsql://localhost/", "SA", "");
            statement = connection.createStatement();
        } catch (SQLException e) {
            e.printStackTrace();
    }
}
       private boolean verificarCredenciales(String correo, String contraseña) {
        try {
            // Realizar la consulta para verificar si el nombre existe
            String query = "SELECT * FROM instituto.profesores WHERE CORREOELECTRONICO = '" + correo +"'" + "AND contrasenia = '"+ contraseña + "'";
            resultSet = statement.executeQuery(query);
            return resultSet.next(); // Si hay al menos una fila, el nombre existe
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
       }
       
        
     
 
